class ConvertYearsIntoDays{
    public static void main(String[] args){
        // Write your code to convert Years into Days (Consider 1 year = 365 Days)
        // Years = 2 -> Days = 730 Days
        // Print number of Days on console
        int years = 2;
        int days = years * 365;
        System.out.println(years + " years is approximately " + days + " days.");
    }
}